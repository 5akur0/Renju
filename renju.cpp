#include <bits/stdc++.h>
using namespace std;

const int N = 15;
int board[N][N];

void Initialize() {
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            board[i][j] = 0;
        }
    }
}

void Print() {
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            cout << board[i][j] << " ";
        }
        cout << endl;
    }
}

int main() {
    Initialize();
    Print();
    return 0;
}
